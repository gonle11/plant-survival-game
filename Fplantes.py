from tkinter import *

def plante1(Pan):
    print("plante 1")
    plt1=Toplevel(Pan)
    canva1=Canvas(plt1,width=300, height=200)
    canva1.bg=PhotoImage(file="bgpanneau.png")
    canva1.create_image(150,100,image=canva1.bg)
    canva1.create_text(150,25,text="Plante 1",font=("Times",25))
    canva1.pack()

